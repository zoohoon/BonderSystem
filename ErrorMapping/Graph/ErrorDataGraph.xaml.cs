        private void ErrorDataGraph_Loaded(object sender, RoutedEventArgs e)
        {
            try
            {
                // Create a DataSeries of type X=double, Y=double
                var LinearDataX = new XyDataSeries<double, double>() { SeriesName = "Linear X" };
                var AngularDataX = new XyDataSeries<double, double>() { SeriesName = "Angular X" };
                var StraightDataX = new XyDataSeries<double, double>() { SeriesName = "Straight X" };
                var LinearDataY = new XyDataSeries<double, double>() { SeriesName = "Linear Y" };
                var AngularDataY = new XyDataSeries<double, double>() { SeriesName = "Angular Y" };
                var StraightDataY = new XyDataSeries<double, double>() { SeriesName = "Straight Y" };

                //defalutSetting();
                SettingErrorData();
                LinearSeriesX.DataSeries = LinearDataX;
                AngularSeriesX.DataSeries = AngularDataX;
                StraightSeriesX.DataSeries = StraightDataX;

                LinearSeriesY.DataSeries = LinearDataY;
                AngularSeriesY.DataSeries = AngularDataY;
                StraightSeriesY.DataSeries = StraightDataY;

                //LinearSeries.
                //var data = DataManager.Instance.GetSinewave(1.0, 0.0, 100, 25);

                // Append data to series. SciChart automatically redraws
                LinearDataX.Append(LPosX, LValueX);
                AngularDataX.Append(APosX, AValueX);
                StraightDataX.Append(SPosX, SValueX);

                LinearDataY.Append(LPosY, LValueY);
                AngularDataY.Append(APosY, AValueY);
                StraightDataY.Append(SPosY, SValueY);

